public class Print {
    public static void main(String[] args) {
        //println
        System.out.println("Hello, I am learning Java!");
        System.out.println("Hello,I am learning Java!");

        //print
        System.out.print("Hello, I am learning Java!");
        System.out.print("Hello,I am learning Java!");

        // \n
        System.out.println("\n");
        System.out.println("Hello, I am learning Java!\n");
        System.out.println("Hello,I am learning Java!\n");
    }
}

/*
println
- print a line
- print a line and move the cursor to the next line

print
- print a line
- print a line and keep the cursor on the same line

\n
- new line
*/